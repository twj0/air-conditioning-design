def write_md(path: Path) -> None:
    lines = [f"# 答辩逐字稿（详细版）", "", TITLE, ""]
    for label, para in SPEECH:
        lines += [f"## {label}", "", para, ""]
    path.write_text("\n".join(lines), encoding="utf-8")


def write_tex(path: Path) -> None:
    body = "\n\n".join(f"\\section*{{{label}}}\n\n{para}" for label, para in SPEECH)
    tex = ("\\documentclass[11pt]{ctexart}\n"
           "\\usepackage[a4paper,margin=2.4cm]{geometry}\n"
           "\\usepackage{setspace}\n"
           "\\onehalfspacing\n"
           f"\\title{{答辩逐字稿（详细版）\\\\\\large {TITLE}}}\n"
           "\\author{作者信息答辩时填写}\n"
           "\\date{2026年6月}\n"
           "\\begin{document}\n"
           "\\maketitle\n"
           f"{body}\n"
           "\\end{document}\n")
    path.write_text(tex, encoding="utf-8")


def write_docx(path: Path) -> None:
    from docx import Document
    from docx.shared import Pt
    from docx.enum.text import WD_ALIGN_PARAGRAPH
    doc = Document()
    style = doc.styles["Normal"]
    style.font.name = "宋体"
    style.font.size = Pt(12)
    h = doc.add_paragraph()
    r = h.add_run("答辩逐字稿（详细版）")
    r.font.name = "宋体"; r.font.size = Pt(18); r.font.bold = True
    h.alignment = WD_ALIGN_PARAGRAPH.CENTER
    sub = doc.add_paragraph()
    sr = sub.add_run(TITLE)
    sr.font.name = "宋体"; sr.font.size = Pt(12)
    sub.alignment = WD_ALIGN_PARAGRAPH.CENTER
    doc.add_paragraph()
    for label, para in SPEECH:
        sh = doc.add_paragraph()
        shr = sh.add_run(label)
        shr.font.name = "宋体"; shr.font.size = Pt(14); shr.font.bold = True
        p = doc.add_paragraph()
        pr = p.add_run(para)
        pr.font.name = "宋体"; pr.font.size = Pt(12)
        p.paragraph_format.first_line_indent = Pt(24)
        doc.add_paragraph()
    doc.save(str(path))


