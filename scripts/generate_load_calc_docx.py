def set_cell_bg(cell, hex_color: str):
    """Set table cell background color (e.g. 'D9D9D9')."""
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), hex_color)
    tcPr.append(shd)


def set_cell_borders(table):
    """Apply thin borders to all cells in the table."""
    tbl = table._tbl
    for tr in tbl.iter_children():
        if tr.tag.endswith("}tr"):
            for tc in tr:
                if tc.tag.endswith("}tc"):
                    tcPr = tc.get_or_add_tcPr()
                    tcBorders = OxmlElement("w:tcBorders")
                    for side in ("top", "left", "bottom", "right", "insideH", "insideV"):
                        border = OxmlElement(f"w:{side}")
                        border.set(qn("w:val"), "single")
                        border.set(qn("w:sz"), "4")
                        border.set(qn("w:space"), "0")
                        border.set(qn("w:color"), "000000")
                        tcBorders.append(border)
                    tcPr.append(tcBorders)


def style_heading(doc: Document, text: str, level: int):
    p = doc.add_heading(text, level=level)
    run = p.runs[0] if p.runs else p.add_run(text)
    run.font.name = "宋体"
    run.font.bold = True
    run.font.size = Pt(14) if level == 1 else Pt(12)
    run.font.color.rgb = RGBColor(0, 0, 0)
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(6)
    return p


def add_para(doc: Document, text: str, bold=False, indent=True):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.name = "宋体"
    run.font.size = Pt(12)
    run.font.bold = bold
    if indent:
        p.paragraph_format.first_line_indent = Cm(0.74)
    p.paragraph_format.space_after = Pt(4)
    return p


def add_caption(doc: Document, text: str):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.name = "宋体"
    run.font.size = Pt(11)
    run.font.bold = True
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after = Pt(2)


def add_table(doc: Document, headers: list[str], rows: list[list[str]],
              col_widths: list[float] | None = None, header_bg="D9E1F2") -> None:
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = "Table Grid"

    # Header row
    hdr_cells = table.rows[0].cells
    for i, h in enumerate(headers):
        hdr_cells[i].text = h
        hdr_cells[i].paragraphs[0].runs[0].font.bold = True
        hdr_cells[i].paragraphs[0].runs[0].font.size = Pt(10)
        hdr_cells[i].paragraphs[0].runs[0].font.name = "宋体"
        hdr_cells[i].paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
        set_cell_bg(hdr_cells[i], header_bg)
        hdr_cells[i].vertical_alignment = WD_ALIGN_VERTICAL.CENTER

    # Data rows
    for r_idx, row in enumerate(rows):
        cells = table.rows[r_idx + 1].cells
        for c_idx, val in enumerate(row):
            cells[c_idx].text = str(val)
            cells[c_idx].paragraphs[0].runs[0].font.size = Pt(10)
            cells[c_idx].paragraphs[0].runs[0].font.name = "宋体"
            cells[c_idx].paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER
            cells[c_idx].vertical_alignment = WD_ALIGN_VERTICAL.CENTER

    # Column widths
    if col_widths:
        for col_i, w in enumerate(col_widths):
            for cell in table.columns[col_i].cells:
                cell.width = Cm(w)

    doc.add_paragraph()  # spacing after table


